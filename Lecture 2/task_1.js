let firstNum = Number(prompt('Enter first number: '))
let secondNum = Number(prompt('Enter second number: '))

let sum = firstNum + secondNum
document.write("Sum: " + sum)

let sub = firstNum - secondNum
document.write("<br> Subtraction: " + sub)

let multi = firstNum * secondNum
document.write("<br> Multiplication: " + multi)

let div = firstNum / secondNum
document.write("<br> Division: " + div)

let modulus = firstNum % secondNum
document.write("<br> Modulus: " + modulus)