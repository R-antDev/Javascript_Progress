
document.write('<p style="color: red" >For Loop</p>')
for (let i = 10; i >= 5; i--)
    document.write(i, '<br>' )


document.write('<p style="color: red" >While Loop</p>')
let i = 1
while (i <= 10) {
    document.write(i, '<br>')
    i ++
}

document.write('<p style="color: red" >Do while Loop</p>')
let a = 1
do {
    document.write(a ,'<br>')
    a++
} while (a < 10)
